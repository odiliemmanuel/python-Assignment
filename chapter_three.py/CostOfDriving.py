distance = float(input("Enter The Distance To Drive: "))
 
milesPerGallon = float(input("The Fuel Efficiency Of The Car In Miles Per Gallon is: "))
                
pricePerGallon = float(input("The Price Per Gallon Of The Car is: "))
                
cost = distance / milesPerGallon * pricePerGallon

print("The Total Cost Of The Driving is: ", cost)
            
        
